function parimpar(n) { // n é uma variavel
    if(n%2 == 0) {
        return 'Par!'
    } else {
        return 'Ímpar!'
    }
}

console.log(parimpar(223)) // retorna o valor lá para cima
// ele verifica se o valor é par ou impar se o resto da divisão por 2 for igual a 0
